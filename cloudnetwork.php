        Cloud computing supports centralised resources accessible to authorised clients remotely, however unlike housing these management resources within a data center infrastructure with dedicated hardware, they are hosted within the cloud on its distributed servers. Cloud deployment has three categories: public clouds operated by third parties; private clouds containing services and infrastructure of a private network; and hybrid clouds combining both private and public. Innovations and improvements within accessibility, IoT devices and WAN bandwidth have aided the expansion of cloud networking and management. Fundamental characteristics include:      </p>
