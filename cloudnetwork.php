      <p class="content">
        Software as a service (SaaS) provides clients with remote access to an application through either thin (browser) or thick (program interface) connections to the cloud. Holds a faster migration than PaaS however it requires support to integrate and restructure. Employee training is also a resource that would need to be considered in this migration. Platform as a service (PaaS) offers the customer the ability to implement the cloud upon independent hardware and applicatins aklowing for higher levels of control for development and application deployment. However, migrating to PaaS would require in-depth support due to the integration requirements with proprietary applications.
      </p>

