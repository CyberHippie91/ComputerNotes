      </blockquote>

      <p class="content">
        Infrastructure as a service (IaaS) is comprised of the provision of hardware requirements and software application. The client has more control and flexibility with this service. Migrating or intergrating to an IaaS Cloud solution would have the least path of resistance compared to PaaS and SaaS due to the lack of architectural change required and would be achieved through lift and shift approach.
      </p>

